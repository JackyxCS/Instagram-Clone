def construct_likes(posts, likers):
    pairs = []
    for post in posts:
        for liker in likers:
            pairs.append((post, liker))

    res = []
    for (post, user) in pairs:
        like = Like(
            post_id=post,
            user_id=user
        )
        res.append(like)
    return res


